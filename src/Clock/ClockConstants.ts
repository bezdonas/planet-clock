export const secsPerCircle: number = 60;
export const minsPerCircle: number = 60;
export const hoursPerCircle: number = 12;
export const degsPerCircle: number = 360;
export const degsPerSec: number = degsPerCircle / secsPerCircle;
export const degsPerMin: number = degsPerCircle / minsPerCircle;
export const degsPerHour: number = degsPerCircle / hoursPerCircle;
